_LLM_BUNDLE = None

def load_local_llm():
    global _LLM_BUNDLE
    if _LLM_BUNDLE is not None:
        return _LLM_BUNDLE
    if not CONFIG.get('RUN_LOCAL_LLM_ALPHA', True):
        print('LLM disabled via CONFIG.RUN_LOCAL_LLM_ALPHA=False; using deterministic fallback.')
        _LLM_BUNDLE = {'backend': 'deterministic_proxy'}
        return _LLM_BUNDLE
    if CONFIG.get('LLM_FORCE_DETERMINISTIC_ON_CPU', True) and not torch.cuda.is_available():
        print('No GPU detected; LLM alpha uses deterministic fallback. Set LLM_FORCE_DETERMINISTIC_ON_CPU=False to run CPU LLM.')
        _LLM_BUNDLE = {'backend': 'deterministic_proxy'}
        return _LLM_BUNDLE
    try:
        from transformers import AutoTokenizer, AutoModelForCausalLM
        model_id = CONFIG['LLM_MODEL_ID']
        print(f'Loading local LLM: {model_id} (cuda_available={torch.cuda.is_available()})')
        tokenizer = AutoTokenizer.from_pretrained(model_id, trust_remote_code=False)
        model = AutoModelForCausalLM.from_pretrained(
            model_id,
            torch_dtype=torch.float16 if torch.cuda.is_available() else 'auto',
            device_map='auto' if torch.cuda.is_available() else None,
            trust_remote_code=False,
        )
        if not torch.cuda.is_available():
            model = model.to('cpu')
        model.eval()
        device = next(model.parameters()).device
        # Pre-compute a stop token list: model EOS plus '}' so greedy decode halts at end of JSON
        eos_ids = [tokenizer.eos_token_id]
        try:
            brace_ids = tokenizer.encode('}', add_special_tokens=False)
            if len(brace_ids) == 1 and brace_ids[0] not in eos_ids:
                eos_ids.append(brace_ids[0])
        except Exception:
            pass
        _LLM_BUNDLE = {
            'backend': 'local_hf',
            'model': model,
            'tokenizer': tokenizer,
            'model_id': model_id,
            'device': device,
            'eos_ids': eos_ids,
        }
        print(f'LLM ready on device={device}, dtype={next(model.parameters()).dtype}, eos_ids={eos_ids}')
        return _LLM_BUNDLE
    except Exception as exc:
        print('Local LLM failed; using deterministic fallback:', repr(exc))
        _LLM_BUNDLE = {'backend': 'deterministic_proxy', 'error': repr(exc)}
        return _LLM_BUNDLE



def v84_purged_action_fold_report(action_specs):
    rows = []
    start = pd.Timestamp(CONFIG['TRAIN_START'])
    final = pd.Timestamp(CONFIG['VALIDATION_END'])
    fold_days = int(CONFIG['V84_PURGED_FOLD_DAYS'])

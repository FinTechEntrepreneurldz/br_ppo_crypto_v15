def _clean_datetime_index(idx):
    idx = pd.to_datetime(idx)
    try:
        if getattr(idx, 'tz', None) is not None:
            idx = idx.tz_localize(None)
    except Exception:
        pass
    return idx


def v8_series(x, name=None):
    s = pd.Series(x).copy()
    s.index = _clean_datetime_index(s.index)
    s = s.sort_index().replace([np.inf, -np.inf], np.nan).fillna(0.0)
    if name is not None:
        s.name = name
    return s


def v8_compound(x):
    x = pd.Series(x).replace([np.inf, -np.inf], np.nan).dropna()
    if len(x) == 0:
        return 0.0
    return float((1.0 + x).prod() - 1.0)


def annualized_sharpe(x):
    x = pd.Series(x).replace([np.inf, -np.inf], np.nan).dropna()
    if len(x) < 20 or x.std() <= 0:
        return np.nan
    return float(x.mean() / x.std() * np.sqrt(252))


def max_drawdown(x):
    x = pd.Series(x).replace([np.inf, -np.inf], np.nan).dropna()
    if len(x) == 0:
        return np.nan
    eq = (1.0 + x).cumprod()
    return float((eq / eq.cummax() - 1.0).min())


def perf_stats(x, start=None, end=None):
    s = v8_series(x)
    if start is not None:
        s = s[s.index >= pd.Timestamp(start)]
    if end is not None:
        s = s[s.index <= pd.Timestamp(end)]
    s = s.dropna()
    if len(s) < 20:
        return dict(ann_return=np.nan, ann_vol=np.nan, sharpe=np.nan, sortino=np.nan, tstat=np.nan,
                    max_dd=np.nan, calmar=np.nan, total_return=np.nan, hit_rate=np.nan, n_days=len(s))
    eq = (1.0 + s).cumprod()
    ann_return = eq.iloc[-1] ** (252.0 / len(s)) - 1.0
    ann_vol = s.std() * np.sqrt(252)
    sharpe = s.mean() / s.std() * np.sqrt(252) if s.std() > 0 else np.nan
    downside = s[s < 0].std() * np.sqrt(252)
    sortino = s.mean() * 252.0 / downside if downside and downside > 0 else np.nan
    tstat = s.mean() / s.std() * np.sqrt(len(s)) if s.std() > 0 else np.nan
    mdd = max_drawdown(s)
    calmar = ann_return / abs(mdd) if mdd and mdd < 0 else np.nan
    return dict(ann_return=ann_return, ann_vol=ann_vol, sharpe=sharpe, sortino=sortino, tstat=tstat,
                max_dd=mdd, calmar=calmar, total_return=eq.iloc[-1] - 1.0, hit_rate=(s > 0).mean(), n_days=len(s))


def apply_vol_target(ret, target_vol=0.25, lookback=63, max_leverage=1.0):
    ret = v8_series(ret)
    if target_vol is None or target_vol <= 0:
        return ret
    vol = ret.rolling(lookback, min_periods=max(20, lookback // 2)).std().shift(1) * np.sqrt(252)
    scale = (float(target_vol) / vol).replace([np.inf, -np.inf], np.nan).clip(0.0, float(max_leverage)).fillna(0.0)
    return (ret * scale).rename(ret.name)



def apply_switch_cost_stress(ret, actions, extra_bps):
    out = v8_series(ret).copy()
    if actions is None or actions.empty:
        return out
    for _, row in actions.iterrows():
        if bool(row.get('switched', False)):
            dt = pd.Timestamp(row['date'])
            next_idx = out.index[out.index > dt]
            if len(next_idx):
                out.loc[next_idx[0]] -= float(extra_bps) / 10000.0
    return out.rename(f'{getattr(ret, "name", "ret")}_cost_{extra_bps}bps')


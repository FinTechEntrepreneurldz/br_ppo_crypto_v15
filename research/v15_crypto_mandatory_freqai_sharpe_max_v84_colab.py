    btc_px = crypto_close['BTC-USD'].reindex(idx).ffill() if 'BTC-USD' in crypto_close.columns else crypto_close.mean(axis=1).reindex(idx).ffill()
    eth_px = crypto_close['ETH-USD'].reindex(idx).ffill() if 'ETH-USD' in crypto_close.columns else btc_px
    out['btc_above_50dma'] = (btc_px > btc_px.rolling(50, min_periods=25).mean()).astype(float)
    out['btc_above_200dma'] = (btc_px > btc_px.rolling(200, min_periods=80).mean()).astype(float)
    out['eth_above_50dma'] = (eth_px > eth_px.rolling(50, min_periods=25).mean()).astype(float)
    out['eth_above_200dma'] = (eth_px > eth_px.rolling(200, min_periods=80).mean()).astype(float)

    px = crypto_close.reindex(idx).ffill()
    ma50 = px.rolling(50, min_periods=25).mean()
    ma200 = px.rolling(200, min_periods=80).mean()
    out['crypto_breadth_50dma'] = (px > ma50).mean(axis=1)
    out['crypto_breadth_200dma'] = (px > ma200).mean(axis=1)
    out['crypto_dispersion_21'] = crypto_ret.reindex(idx).rolling(21, min_periods=10).std().mean(axis=1)
    out['crypto_vol_21'] = crypto_ret.reindex(idx).mean(axis=1).rolling(21, min_periods=10).std() * np.sqrt(252)
    out['crypto_vol_63'] = crypto_ret.reindex(idx).mean(axis=1).rolling(63, min_periods=21).std() * np.sqrt(252)

    btc_eq = (1.0 + btc).cumprod()
    out['btc_drawdown_63'] = btc_eq / btc_eq.rolling(63, min_periods=21).max() - 1.0
    eth_eq = (1.0 + eth).cumprod()
    out['eth_drawdown_63'] = eth_eq / eth_eq.rolling(63, min_periods=21).max() - 1.0

    # Correlation is expensive to compute as a full rolling tensor; this loop is cheap because it runs only on allocation dates.
    corr_map = {}
    for dt in alloc_dates:
        dt = pd.Timestamp(dt)
        hist = crypto_ret.loc[:dt].tail(63)
        if hist.shape[0] < 30 or hist.shape[1] < 3:
            corr_map[dt] = np.nan
        else:
            cm = hist.corr().values
            corr_map[dt] = float(np.nanmean(cm[np.triu_indices_from(cm, k=1)]))
    out['crypto_avg_corr_63'] = pd.Series(corr_map).reindex(idx).ffill()

    if 'QQQ' in etf_ret.columns and 'SPY' in etf_ret.columns:
        out['qqq_minus_spy_63'] = (etf_ret['QQQ'] - etf_ret['SPY']).reindex(idx).rolling(63, min_periods=21).sum()
    else:
        out['qqq_minus_spy_63'] = 0.0

    vol_med = out['crypto_vol_63'].expanding(min_periods=80).median()
    out['crypto_risk_on_score'] = (
        0.18 * out['btc_above_50dma'] +
        0.18 * out['btc_above_200dma'] +
        0.14 * out['eth_above_50dma'] +
        0.14 * out['eth_above_200dma'] +
        0.18 * out['crypto_breadth_50dma'] +
        0.10 * out['crypto_breadth_200dma'] +
        0.08 * (out['qqq_minus_spy_63'] > 0).astype(float)
    ).clip(0.0, 1.0)
    out['bad_crypto_regime'] = (
        (out['crypto_risk_on_score'] < float(CONFIG['CRYPTO_BAD_REGIME_RISK_SCORE'])) |
        (out['btc_drawdown_63'] < float(CONFIG['CRYPTO_BTC_DD_RISK_OFF'])) |
        (out['crypto_vol_63'] > 1.65 * vol_med)
    ).astype(float)
    return out.replace([np.inf, -np.inf], np.nan).fillna(0.0)


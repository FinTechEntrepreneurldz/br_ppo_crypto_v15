class V8CryptoMandatoryAlphaPPOEnv(gym.Env):
    metadata = {'render_modes': []}
    def __init__(self, decision_dates, reward_cfg=None):
        super().__init__()
        self.decision_dates = [pd.Timestamp(d) for d in decision_dates]
        if len(self.decision_dates) == 0:
            raise RuntimeError('V8CryptoMandatoryAlphaPPOEnv received no decision dates.')
        self.reward_cfg = reward_cfg or V8_REWARD_TRIAL_CONFIGS[0]
        self.n_features = len(v8_feature_cols) + len(V8_ACTION_NAMES)
        self.observation_space = spaces.Box(low=-10.0, high=10.0, shape=(self.n_features,), dtype=np.float32)
        self.action_space = spaces.Discrete(len(V8_ACTION_NAMES))
        self.t = 0
        self.last_action_idx = 0
        self.action_counts = np.zeros(len(V8_ACTION_NAMES), dtype=float)
    def reset(self, seed=None, options=None):
        super().reset(seed=seed)
        self.t = 0
        self.last_action_idx = 0
        self.action_counts[:] = 0.0
        return v8_obs_for_date(self.decision_dates[self.t], self.last_action_idx), {}
    def step(self, action):
        action = int(action)
        dt = self.decision_dates[self.t]
        next_dt = pd.Timestamp(v8_state_df.loc[dt, 'next_date'])
        days = stream_df.index[(stream_df.index > dt) & (stream_df.index <= next_dt)]
        raw_action_name = V8_ACTION_NAMES[action]
        action_name = v82_effective_action_name(raw_action_name, days) if 'v82_effective_action_name' in globals() else raw_action_name
        if len(days) == 0:
            interval_ret = pd.Series(0.0, index=[dt])
        else:
            interval_ret = v8_action_interval_return(action_name, days).copy()
            if action != self.last_action_idx:
                interval_ret.iloc[0] -= float(self.reward_cfg['switch_cost_bps']) / 10000.0
        comp = v8_compound(interval_ret)
        hurdle = v8_hurdle_return(interval_ret.index)
        bil_ret = v8_reference_interval_return('BIL', interval_ret.index)
        spy_ret = v8_reference_interval_return('SPY', interval_ret.index)
        top_ret = v8_reference_interval_return('TOP_EW', interval_ret.index)
        vol = float(interval_ret.std() * np.sqrt(252)) if len(interval_ret) > 3 else 0.0
        dd = abs(min(0.0, max_drawdown(interval_ret))) if len(interval_ret) > 3 else 0.0
        under = max(0.0, hurdle - comp)
        action_weights = V8_ACTION_SPECS[action_name]
        crypto_expo = v8_action_crypto_exposure(action_weights)
        bad_regime = v8_bad_crypto_regime_for_days(interval_ret.index)
        excess_crypto_in_bad = max(0.0, crypto_expo - float(CONFIG['V8_PPO_CRYPTO_MAX_BAD_REGIME_EXPOSURE']))
        floor = float(CONFIG['V8_PPO_RETURN_FLOOR_PER_INTERVAL']) * max(1.0, len(interval_ret) / max(1, int(CONFIG['REBALANCE_DAYS'])))
        self.action_counts[action] += 1.0
        dominance = self.action_counts[action] / max(1.0, self.action_counts.sum())
        reward = (
            comp - hurdle
            + float(self.reward_cfg['bil_excess_bonus']) * max(0.0, comp - bil_ret)
            + float(self.reward_cfg['spy_excess_bonus']) * max(0.0, comp - spy_ret)
            + float(self.reward_cfg['top_excess_bonus']) * max(0.0, comp - top_ret)
            + float(self.reward_cfg['return_floor_bonus']) * max(0.0, comp - floor)
            - float(self.reward_cfg['vol_penalty']) * vol * len(interval_ret) / 252.0
            - float(self.reward_cfg['dd_penalty']) * dd
            - float(self.reward_cfg['underperf_penalty']) * under
            - float(self.reward_cfg['bad_regime_penalty']) * bad_regime * excess_crypto_in_bad
            - float(self.reward_cfg['dominance_penalty']) * max(0.0, dominance - 0.50)
        )
        self.last_action_idx = action
        self.t += 1
        terminated = self.t >= len(self.decision_dates)
        obs = np.zeros(self.n_features, dtype=np.float32) if terminated else v8_obs_for_date(self.decision_dates[self.t], self.last_action_idx)
        info = {'date': str(dt.date()), 'raw_action': raw_action_name, 'action': action_name, 'compound_return': comp, 'hurdle': hurdle, 'crypto_exposure': crypto_expo, 'bad_crypto_regime': bad_regime}
        return obs, float(reward * float(CONFIG['V8_PPO_REWARD_SCALE'])), terminated, False, info



        mats[f'vol_{w}'] = ret.rolling(w, min_periods=max(2, w // 2)).std() * np.sqrt(252)
        mats[f'dd_{w}'] = px / px.rolling(w, min_periods=max(3, w // 2)).max() - 1.0
        mats[f'path_eff_{w}'] = (lr.rolling(w, min_periods=max(3, w // 2)).sum().abs() / lr.abs().rolling(w, min_periods=max(3, w // 2)).sum()).replace([np.inf, -np.inf], np.nan)


def deterministic_llm_pick(dt, slate=None):
    dt = pd.Timestamp(dt)
    if slate is None or slate.empty:
        slate = build_llm_slate(dt)
    if slate.empty:
        return {'longs': [], 'shorts': [], 'confidence': 0.0, 'source': 'deterministic_empty', 'notes': 'empty slate'}
    sorted_long = slate.sort_values('alpha_score', ascending=False)['symbol'].tolist()
    sorted_short = slate.sort_values('alpha_score', ascending=True)['symbol'].tolist()
    k = int(CONFIG['LLM_PICK_N'])
    longs = diversified_take(sorted_long, k)
    shorts = diversified_take([s for s in sorted_short if s not in longs], k)
    return {'longs': longs, 'shorts': shorts, 'confidence': 0.50, 'source': 'deterministic_proxy', 'notes': 'fallback from alpha score'}


def extract_json_object(text):
    if not isinstance(text, str):
        return None
    text = text.replace('```json', '```')
    if '```' in text:
        pieces = text.split('```')
        for p in pieces:
            if '{' in p and '}' in p:
                text = p
                break
    start = text.find('{')
    if start < 0:
        return None
    depth = 0
    for i in range(start, len(text)):
        if text[i] == '{':
            depth += 1
        elif text[i] == '}':
            depth -= 1
            if depth == 0:
                try:
                    return json.loads(text[start:i+1])
                except Exception:
                    return None
    return None



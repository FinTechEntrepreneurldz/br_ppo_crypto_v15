def _v84_sample_weight(train_dates, current_dt):
    # FreqAI-style temporal importance: recent observations carry more weight.
    current_dt = pd.Timestamp(current_dt)
    age_days = np.array([(current_dt - pd.Timestamp(d)).days for d in train_dates], dtype=float)
    # Half-life around one year; bounded so old data still contributes.
    w = np.exp(-age_days / 365.0)
    return np.clip(w, 0.15, 1.0)



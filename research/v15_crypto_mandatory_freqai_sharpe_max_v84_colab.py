    fold_start = start + pd.Timedelta(days=365)
    fold_id = 0
    while fold_start + pd.Timedelta(days=fold_days) <= final:
        fold_end = fold_start + pd.Timedelta(days=fold_days)
        # The embargo is reported; action returns themselves are precomputed streams, so no refit occurs here.
        for action, weights in action_specs.items():
            ret = v8_weighted_stream_return(weights, action)
            st = perf_stats(ret, fold_start, fold_end)
            rows.append({'fold_id': fold_id, 'fold_start': fold_start, 'fold_end': fold_end, 'embargo_start': fold_start - embargo, 'embargo_end': fold_end + embargo, 'action': action, **st, 'crypto_exposure': v8_action_crypto_exposure(weights), 'hard_crypto_exposure': v8_action_hard_crypto_exposure(weights), 'crypto_ls_weight': float(weights.get('CRYPTO_LS', 0.0))})
        fold_start += pd.Timedelta(days=step_days)
        fold_id += 1
    return pd.DataFrame(rows)



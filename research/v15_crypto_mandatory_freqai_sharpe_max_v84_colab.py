    diagnostics = []
    if len(train_df) < int(CONFIG['V83_FREQAI_MIN_TRAIN_ROWS']):
        return {'models': models, 'status': 'too_few_rows', 'diag': diagnostics}
    for name, est in _v83_freqai_model_specs().items():
        try:
            pipe = Pipeline([('imputer', SimpleImputer(strategy='median')), ('scaler', StandardScaler()), ('model', est)])
            pipe.fit(X, y)
            pred_train = pipe.predict(X)
            corr = pd.Series(pred_train).rank().corr(pd.Series(y.values).rank())
            models[name] = pipe
            diagnostics.append({'model': name, 'n_train_rows': len(train_df), 'train_rank_corr': corr})
        except Exception as exc:
            diagnostics.append({'model': name, 'n_train_rows': len(train_df), 'train_rank_corr': np.nan, 'error': repr(exc)})
    return {'models': models, 'status': 'ok' if models else 'all_failed', 'diag': diagnostics}



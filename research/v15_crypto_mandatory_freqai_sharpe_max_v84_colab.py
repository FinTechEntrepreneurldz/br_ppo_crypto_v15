def apply_crypto_drawdown_throttle(ret, name=None):
    x = v8_series(ret).reindex(base_index).fillna(0.0)
    prior_eq = (1.0 + x.shift(1).fillna(0.0)).cumprod()
    prior_dd_63 = prior_eq / prior_eq.rolling(63, min_periods=21).max() - 1.0
    prior_dd_126 = prior_eq / prior_eq.rolling(126, min_periods=40).max() - 1.0

    scale = pd.Series(1.0, index=x.index)
    scale = scale.where(prior_dd_63 >= float(CONFIG['CRYPTO_DD_FAST_THRESHOLD']), float(CONFIG['CRYPTO_DD_FAST_SCALE']))
    scale = scale.where(prior_dd_126 >= float(CONFIG['CRYPTO_DD_SLOW_THRESHOLD']), float(CONFIG['CRYPTO_DD_SLOW_SCALE']))
    bad = crypto_regime_df['bad_crypto_regime'].shift(1).reindex(x.index).fillna(0.0) > 0.5
    scale = scale.where(~bad, np.minimum(scale, 0.50))
    out = x * scale + BIL_RET_SERIES * (1.0 - scale)
    return out.rename(name or getattr(ret, 'name', 'throttled_crypto'))



def v84_meta_weights(dt, mode='long', params=None):
    params = dict(params or {})
    long_n = int(params.get('long_n', CONFIG.get('V83_FREQAI_LONG_N_DEFAULT', 4)))
    short_n = int(params.get('short_n', CONFIG.get('V83_FREQAI_SHORT_N_DEFAULT', 4)))
    prob_min = float(params.get('prob_min', CONFIG['V84_META_PROB_MIN_DEFAULT']))
    score_threshold = float(params.get('score_threshold', CONFIG['V84_META_SCORE_THRESHOLD_DEFAULT']))
    di_max = float(params.get('di_max', CONFIG['V84_META_DI_MAX_DEFAULT']))
    min_risk_on = float(params.get('min_risk_on', 0.0))
    dt = pd.Timestamp(dt)
    score = pd.Series(V84_META_SCORE_MAP.get(dt, _v83_prediction_fallback(dt))).reindex(crypto_cols).fillna(0.0)
    prob = pd.Series(V84_META_PROB_MAP.get(dt, pd.Series(0.50, index=crypto_cols))).reindex(crypto_cols).fillna(0.50)
    di = pd.Series(V83_FREQAI_DI_MAP.get(dt, pd.Series(99.0, index=crypto_cols))).reindex(crypto_cols).fillna(99.0) if 'V83_FREQAI_DI_MAP' in globals() else pd.Series(0.0, index=crypto_cols)
    state = crypto_regime_at(dt)
    if float(state.get('crypto_risk_on_score', 0.0)) < min_risk_on:
        return w_crypto_btc_eth_core(dt)
    eligible = score.index[(prob >= prob_min) & (di <= di_max)].tolist()
    if len(eligible) < 2:
        return w_crypto_btc_eth_core(dt)
    s = score.reindex(eligible).dropna()
    longs = s[s >= score_threshold].nlargest(min(long_n, len(s))).index.tolist()
    if not longs:
        longs = s.nlargest(min(long_n, len(s))).index.tolist()
    if mode == 'long':
        return crypto_inverse_vol_weights(longs, dt, gross=1.0)
    shorts = s[s <= -score_threshold].nsmallest(min(short_n, len(s))).index.tolist()
    shorts = [x for x in shorts if x not in longs]
    out = crypto_inverse_vol_weights(longs, dt, gross=0.50 if shorts else 1.0)
    if shorts:
        short_abs = crypto_inverse_vol_weights(shorts, dt, gross=0.50).abs()
        out.loc[short_abs.index] = out.reindex(short_abs.index).fillna(0.0) - short_abs
    return out.reindex(crypto_cols).fillna(0.0)


def build_v84_meta_stream(mode='long', params=None, name='CRYPTO_FREQAI_META_LONG'):
    daily = pd.Series(0.0, index=base_index, name=name)
    rows = []
    current_w = pd.Series(0.0, index=crypto_cols, dtype=float)
    cost = float(CONFIG['CRYPTO_TRANSACTION_COST_BPS']) / 10000.0
    for i, dt in enumerate(alloc_dates):
        dt = pd.Timestamp(dt)
        next_dt = pd.Timestamp(alloc_dates[i + 1]) if i + 1 < len(alloc_dates) else base_index[-1]
        days = base_index[(base_index > dt) & (base_index <= next_dt)]
        if len(days) == 0:
            continue
        new_w = v84_meta_weights(dt, mode=mode, params=params).reindex(crypto_cols).fillna(0.0)
        turnover = float((new_w - current_w).abs().sum())
        ret = crypto_ret.reindex(days).fillna(0.0).dot(new_w)
        short_expo = float(-new_w[new_w < 0].sum())
        if short_expo > 0:
            ret = ret - short_expo * float(CONFIG['CRYPTO_SHORT_FEE_ANNUAL']) / 252.0
        if len(ret):
            ret.iloc[0] -= turnover * cost
        daily.loc[days] = ret.values
        rows.append({'stream': name, 'date': dt, 'next_date': next_dt, 'mode': mode, 'turnover': turnover, 'gross': float(new_w.abs().sum()), 'net': float(new_w.sum()), 'long_gross': float(new_w[new_w > 0].sum()), 'short_gross': short_expo, 'avg_meta_prob': float(pd.Series(V84_META_PROB_MAP.get(dt, pd.Series(index=crypto_cols, dtype=float))).mean()), 'longs': ','.join(new_w[new_w > 0].sort_values(ascending=False).index.tolist()), 'shorts': ','.join(new_w[new_w < 0].sort_values().index.tolist())})
        current_w = new_w
    return daily.fillna(0.0).rename(name), pd.DataFrame(rows)


def v84_meta_threshold_objective(trial):
    params = {
        'long_n': trial.suggest_int('long_n', 2, min(7, len(crypto_cols))),
        'short_n': trial.suggest_int('short_n', 2, min(7, len(crypto_cols))),
        'prob_min': trial.suggest_float('prob_min', 0.50, 0.72),
        'score_threshold': trial.suggest_float('score_threshold', 0.0, 0.65),
        'di_max': trial.suggest_float('di_max', 1.25, 5.25),
        'min_risk_on': trial.suggest_float('min_risk_on', 0.0, 0.55),
    }
    mode = trial.suggest_categorical('mode', ['long', 'ls', 'confidence'])
    ret, _ = build_v84_meta_stream(mode='ls' if mode == 'ls' else 'long', params=params, name='v84_meta_trial')
    val = perf_stats(ret, CONFIG['VALIDATION_START'], CONFIG['VALIDATION_END'])
    core = perf_stats(crypto_streams.get('CRYPTO_BTC_ETH_CORE', ret), CONFIG['VALIDATION_START'], CONFIG['VALIDATION_END'])
    cash = perf_stats(BIL_RET_SERIES, CONFIG['VALIDATION_START'], CONFIG['VALIDATION_END'])
    sharpe = val['sharpe'] if pd.notna(val['sharpe']) else -9.0
    ret_ann = val['ann_return'] if pd.notna(val['ann_return']) else -9.0
    dd = abs(val['max_dd']) if pd.notna(val['max_dd']) else 1.0
    excess_core = sharpe - (core['sharpe'] if pd.notna(core['sharpe']) else 0.0)
    excess_cash = sharpe - (cash['sharpe'] if pd.notna(cash['sharpe']) else 0.0)
    return float(110.0 * sharpe + 20.0 * ret_ann + 25.0 * excess_core + 10.0 * excess_cash - 35.0 * dd)



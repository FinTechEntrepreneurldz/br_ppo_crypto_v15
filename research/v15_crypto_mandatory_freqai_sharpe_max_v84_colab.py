def compute_ichimoku_matrix(close_df, high_df=None, low_df=None, conv=9, base=26, span_b=52):
    high_df = close_df if high_df is None else high_df
    low_df = close_df if low_df is None else low_df

    # Align high/low to close to avoid label-mismatch surprises downstream.
    high_df = high_df.reindex(index=close_df.index, columns=close_df.columns)
    low_df = low_df.reindex(index=close_df.index, columns=close_df.columns)

    tenkan = (high_df.rolling(conv, min_periods=max(3, conv // 2)).max()
              + low_df.rolling(conv, min_periods=max(3, conv // 2)).min()) / 2.0
    kijun = (high_df.rolling(base, min_periods=max(5, base // 2)).max()
             + low_df.rolling(base, min_periods=max(5, base // 2)).min()) / 2.0
    span_a = (tenkan + kijun) / 2.0
    span_b_line = (high_df.rolling(span_b, min_periods=max(10, span_b // 2)).max()
                   + low_df.rolling(span_b, min_periods=max(10, span_b // 2)).min()) / 2.0

    # Elementwise max/min preserves the DataFrame's index AND column order,
    # unlike pd.concat(...).groupby(level=1, axis=1).max() which sorts columns
    # alphabetically and triggers "Can only compare identically-labeled" errors.
    cloud_top = pd.DataFrame(
        np.maximum(span_a.to_numpy(), span_b_line.to_numpy()),
        index=span_a.index, columns=span_a.columns,
    ).reindex(columns=close_df.columns)
    cloud_bot = pd.DataFrame(
        np.minimum(span_a.to_numpy(), span_b_line.to_numpy()),
        index=span_a.index, columns=span_a.columns,
    ).reindex(columns=close_df.columns)

    return {
        'tenkan': tenkan,
        'kijun': kijun,
        'span_a': span_a,
        'span_b': span_b_line,
        'cloud_top': cloud_top,
        'cloud_bot': cloud_bot,
        'above_cloud': (close_df > cloud_top).astype(float),
        'below_cloud': (close_df < cloud_bot).astype(float),
        'tenkan_gt_kijun': (tenkan > kijun).astype(float),
        'cloud_thickness': ((cloud_top - cloud_bot).abs() / close_df).replace([np.inf, -np.inf], np.nan),
        'dist_cloud_mid': (close_df / ((cloud_top + cloud_bot) / 2.0) - 1.0).replace([np.inf, -np.inf], np.nan),
    }


def compute_rsi(df, window=14):
    delta = df.diff()
    up = delta.clip(lower=0.0)
    down = -delta.clip(upper=0.0)
    rs = up.rolling(window, min_periods=window // 2).mean() / down.rolling(window, min_periods=window // 2).mean().replace(0, np.nan)
    return (100.0 - 100.0 / (1.0 + rs)).replace([np.inf, -np.inf], np.nan)


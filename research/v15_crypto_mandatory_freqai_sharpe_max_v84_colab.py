# This overrides the environment/simulation functions defined above before the training loop starts.
# It reduces whipsaw by blending the previous action weights with the new target weights on switches.

CONFIG.setdefault('V84_PARTIAL_REBALANCE_ENABLE', True)
CONFIG.setdefault('V84_PARTIAL_REBALANCE_OLD_WEIGHT', 0.55)
CONFIG.setdefault('V84_PARTIAL_REBALANCE_NEW_WEIGHT', 0.45)
CONFIG.setdefault('V84_BAD_REGIME_FORCE_DEFENSIVE', True)
CONFIG.setdefault('V84_BAD_REGIME_DEFENSIVE_ACTION', 'crypto05_bil95')
CONFIG.setdefault('V84_ACTION_MASK_BAD_REGIME_THRESHOLD', 0.50)
CONFIG.setdefault('V84_ACTION_MASK_MAX_CRYPTO_IN_BAD_REGIME', 0.18)


def v84_blend_action_weights(prev_weights, target_weights):
    if not CONFIG.get('V84_PARTIAL_REBALANCE_ENABLE', True) or prev_weights is None:
        return dict(target_weights)
    old_w = float(CONFIG.get('V84_PARTIAL_REBALANCE_OLD_WEIGHT', 0.55))
    new_w = float(CONFIG.get('V84_PARTIAL_REBALANCE_NEW_WEIGHT', 0.45))
    total = max(1e-12, old_w + new_w)
    old_w, new_w = old_w / total, new_w / total
    keys = set(prev_weights.keys()) | set(target_weights.keys())
    out = {k: old_w * float(prev_weights.get(k, 0.0)) + new_w * float(target_weights.get(k, 0.0)) for k in keys}
    out = {k: v for k, v in out.items() if k in stream_df.columns and abs(v) > 1e-12}
    s = sum(out.values())
    if abs(s) > 1e-12:
        out = {k: v / s for k, v in out.items()}
    return out



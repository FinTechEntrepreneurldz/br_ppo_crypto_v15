    for dt in [pd.Timestamp(d) for d in decision_dates]:
        if dt not in v8_state_df.index:
            continue
        obs = v8_obs_for_date(dt, last_action_idx)
        probs_stack = [action_probabilities_for_obs(model, obs) for model in models]
        avg_probs = np.mean(np.vstack(probs_stack), axis=0)
        action = int(np.argmax(avg_probs))

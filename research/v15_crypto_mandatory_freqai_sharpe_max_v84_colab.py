def build_rebalanced_equity_stream(weight_func, name):
    daily = pd.Series(0.0, index=base_index, dtype=float, name=name)
    rows = []
    current_w = pd.Series(0.0, index=symbols)
    cost = float(CONFIG['ALPHA_TRANSACTION_COST_BPS']) / 10000.0
    borrow = float(CONFIG['ALPHA_BORROW_FEE_ANNUAL'])
    for i, dt in enumerate(alloc_dates):
        dt = pd.Timestamp(dt)
        next_dt = alloc_dates[i + 1] if i + 1 < len(alloc_dates) else base_index[-1]
        days = base_index[(base_index > dt) & (base_index <= next_dt)]
        if len(days) == 0:
            continue
        new_w = pd.Series(weight_func(dt), index=symbols).replace([np.inf, -np.inf], np.nan).fillna(0.0)
        turnover = float((new_w - current_w).abs().sum())
        short_expo = float(-new_w[new_w < 0].sum())
        ret = R.loc[days].dot(new_w)
        if short_expo > 0:
            ret = ret - short_expo * borrow / 252.0
        if len(ret) > 0:
            ret.iloc[0] -= turnover * cost
        daily.loc[days] = ret.values
        rows.append({'stream': name, 'date': dt, 'next_date': next_dt, 'turnover': turnover, 'gross': float(new_w.abs().sum()), 'net': float(new_w.sum()), 'n_long': int((new_w > 0).sum()), 'n_short': int((new_w < 0).sum()), 'longs': ','.join(new_w[new_w > 0].sort_values(ascending=False).index[:30]), 'shorts': ','.join(new_w[new_w < 0].sort_values().index[:30])})
        current_w = new_w
    daily = apply_vol_target(daily, target_vol=float(CONFIG['ALPHA_TARGET_VOL']), lookback=63, max_leverage=float(CONFIG['ALPHA_MAX_LEVERAGE'])).rename(name)
    return daily, pd.DataFrame(rows)


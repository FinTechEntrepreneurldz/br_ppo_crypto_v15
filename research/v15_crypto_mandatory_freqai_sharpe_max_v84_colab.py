def v84_meta_maps():
    prob_map, score_map = {}, {}
    if V84_META_PREDICTIONS.empty:
        return prob_map, score_map
    pred = V84_META_PREDICTIONS.copy()
    pred['date'] = pd.to_datetime(pred['date'])
    base_score_map = V83_FREQAI_SCORE_MAP if 'V83_FREQAI_SCORE_MAP' in globals() else {}
    for dt, g in pred.groupby('date'):
        dt = pd.Timestamp(dt)
        prob = pd.Series(g['meta_prob'].values, index=g['symbol'].values).reindex(crypto_cols).fillna(0.50)
        base_score = pd.Series(base_score_map.get(dt, _v83_prediction_fallback(dt))).reindex(crypto_cols).fillna(0.0)
        # Meta score shrinks predictions whose probability is below 0.5.
        score = base_score * (2.0 * prob - 1.0).clip(lower=-1.0, upper=1.0)
        prob_map[dt] = prob
        score_map[dt] = robust_z(score).fillna(0.0)
    return prob_map, score_map



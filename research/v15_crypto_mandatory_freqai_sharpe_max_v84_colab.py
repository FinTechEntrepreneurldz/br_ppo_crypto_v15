def robust_z(x, clip=4.0):
    x = pd.Series(x, dtype=float).replace([np.inf, -np.inf], np.nan)
    med = x.median()
    mad = (x - med).abs().median()
    if pd.isna(mad) or mad <= 1e-12:
        sd = x.std()
        if pd.isna(sd) or sd <= 1e-12:
            return x * np.nan
        z = (x - x.mean()) / sd
    else:
        z = 0.6745 * (x - med) / mad
    return z.clip(-clip, clip)


def cap_positive_weights(w, cap=0.06, target=1.0):
    w = pd.Series(w, dtype=float).replace([np.inf, -np.inf], np.nan).fillna(0.0).clip(lower=0.0)
    if w.sum() <= 0 or target <= 0:
        return w * 0.0
    w = w / w.sum() * float(target)
    cap = float(cap)
    if cap <= 0:
        return w
    for _ in range(25):
        over = w > cap + 1e-12
        if not over.any():
            break
        fixed = w[over].clip(upper=cap)
        free = w[~over]
        remaining = float(target) - fixed.sum()
        if remaining <= 0 or free.empty:
            return pd.concat([fixed, free * 0.0]).reindex(w.index).fillna(0.0)
        free = free / free.sum() * remaining if free.sum() > 0 else pd.Series(remaining / len(free), index=free.index)
        w = pd.concat([fixed, free]).reindex(w.index).fillna(0.0)
    return w


def extract_field(raw_df, field):
    if isinstance(raw_df.columns, pd.MultiIndex):
        if field in raw_df.columns.get_level_values(1):
            out = raw_df.xs(field, axis=1, level=1)
        elif field in raw_df.columns.get_level_values(0):
            out = raw_df.xs(field, axis=1, level=0)
        else:
            raise ValueError(f'Cannot find field {field} in MultiIndex columns')
        out.columns = [str(c) for c in out.columns]
        return out
    if field in raw_df.columns:
        return raw_df[[field]]
    raise ValueError(f'Cannot find field {field}')



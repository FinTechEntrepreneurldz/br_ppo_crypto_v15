def v8_compare_to_streams(ret, start, end=None, refs=None):
    refs = refs or [c for c in ['BIL', 'SPY', 'QQQ', 'TOP_EW', 'CURRENT_EW', 'V8_ALPHA_ENSEMBLE', 'V8_ALPHA_LLM_LONG', 'CRYPTO_RISK_PARITY', 'CRYPTO_LONG_MOM', 'CRYPTO_CASH_GATED'] if c in stream_df.columns]
    rows = []
    s_stats = perf_stats(ret, start, end)
    for ref in refs:
        r_stats = perf_stats(stream_df[ref], start, end)
        active = pd.concat([pd.Series(ret).rename('strategy'), stream_df[ref].rename(ref)], axis=1).loc[pd.Timestamp(start):].dropna()
        if end is not None:
            active = active.loc[:pd.Timestamp(end)]
        ir = np.nan
        if len(active) > 20:
            diff = active['strategy'] - active[ref]
            ir = annualized_sharpe(diff)
        rows.append({'reference': ref, 'strategy_sharpe': s_stats['sharpe'], 'reference_sharpe': r_stats['sharpe'], 'sharpe_diff': s_stats['sharpe'] - r_stats['sharpe'] if pd.notna(s_stats['sharpe']) and pd.notna(r_stats['sharpe']) else np.nan, 'information_ratio_vs_ref': ir, 'strategy_ann_return': s_stats['ann_return'], 'reference_ann_return': r_stats['ann_return'], 'return_diff': s_stats['ann_return'] - r_stats['ann_return'] if pd.notna(s_stats['ann_return']) and pd.notna(r_stats['ann_return']) else np.nan, 'strategy_max_dd': s_stats['max_dd'], 'reference_max_dd': r_stats['max_dd']})
    return pd.DataFrame(rows)


def v8_summary_score(ret, name):
    val_stats = perf_stats(ret, CONFIG['VALIDATION_START'], CONFIG['VALIDATION_END'])
    val_cmp = v8_compare_to_streams(ret, CONFIG['VALIDATION_START'], CONFIG['VALIDATION_END'])
    min_sharpe_diff = val_cmp['sharpe_diff'].min()
    avg_sharpe_diff = val_cmp['sharpe_diff'].mean()
    min_return_diff = val_cmp['return_diff'].min()
    n_sharpe_beaten = int((val_cmp['sharpe_diff'] > 0).sum())
    n_return_beaten = int((val_cmp['return_diff'] > 0).sum())
    bil_rows = val_cmp[val_cmp['reference'].eq('BIL')]
    bil_excess = float(bil_rows['information_ratio_vs_ref'].iloc[0]) if len(bil_rows) else np.nan
    score = 1000.0 * n_sharpe_beaten + 240.0 * (min_sharpe_diff if pd.notna(min_sharpe_diff) else -9.0) + 120.0 * (avg_sharpe_diff if pd.notna(avg_sharpe_diff) else -9.0) + 125.0 * n_return_beaten + 40.0 * (bil_excess if pd.notna(bil_excess) else 0.0) + 25.0 * (val_stats['sharpe'] if pd.notna(val_stats['sharpe']) else -9.0) + 20.0 * (val_stats['ann_return'] if pd.notna(val_stats['ann_return']) else -9.0) - 30.0 * abs(val_stats['max_dd'] if pd.notna(val_stats['max_dd']) else 0.0)
    row = {'strategy': name, 'v8_score': score, 'validation_sharpe': val_stats['sharpe'], 'validation_ann_return': val_stats['ann_return'], 'validation_max_dd': val_stats['max_dd'], 'n_refs_sharpe_beaten': n_sharpe_beaten, 'n_refs_return_beaten': n_return_beaten, 'min_sharpe_diff': min_sharpe_diff, 'avg_sharpe_diff': avg_sharpe_diff, 'min_return_diff': min_return_diff, 'bil_excess_ir': bil_excess}
    return score, val_cmp, row



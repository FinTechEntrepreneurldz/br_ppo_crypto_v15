def build_v82_simple_meta_allocator():
    """Top-k trailing score allocator. Keeps a crypto floor and a BIL safety cap."""
    candidates = v82_candidate_stream_cols()
    daily = pd.Series(0.0, index=stream_df.index, name='V82_SIMPLE_META_ALLOCATOR')
    rows = []
    for i, dt in enumerate(alloc_dates):
        dt = pd.Timestamp(dt)
        next_dt = pd.Timestamp(alloc_dates[i + 1]) if i + 1 < len(alloc_dates) else stream_df.index.max()
        days = stream_df.index[(stream_df.index > dt) & (stream_df.index <= next_dt)]
        if len(days) == 0:
            continue
        score = {}
        for c in candidates:
            past = stream_df[c].loc[:dt].tail(63)
            sh = annualized_sharpe(past) if len(past) > 20 else 0.0
            ret63 = v8_compound(past)
            dd = abs(min(0.0, max_drawdown(past))) if len(past) > 20 else 0.0
            vol = float(past.std() * np.sqrt(252)) if len(past) > 5 else 0.0
            score[c] = 0.45 * sh + 1.25 * ret63 - 0.80 * dd - 0.25 * vol
        ranked = pd.Series(score).replace([np.inf, -np.inf], np.nan).dropna().sort_values(ascending=False)
        selected = ranked.head(int(CONFIG['V82_SIMPLE_META_TOP_K'])).index.tolist()
        weights = pd.Series(0.0, index=candidates, dtype=float)
        if selected:
            vals = ranked.loc[selected].clip(lower=0.0)
            if vals.sum() <= 0:
                vals = pd.Series(1.0, index=selected)
            weights.loc[selected] = vals / vals.sum()
        else:
            if 'BIL' in candidates:
                weights.loc['BIL'] = 1.0
        # Ensure the internal allocator itself has a crypto floor.
        crypto_cols = [c for c in ['CRYPTO_CASH_GATED', 'CRYPTO_BTC_ETH_CORE', 'CRYPTO_LOW_VOL_CORE', 'CRYPTO_RISK_PARITY'] if c in weights.index]
        crypto_floor = float(CONFIG['V82_META_CRYPTO_FLOOR'])
        crypto_now = float(weights.reindex(crypto_cols).fillna(0.0).sum()) if crypto_cols else 0.0
        if crypto_cols and crypto_now < crypto_floor:
            scale = max(0.0, 1.0 - crypto_floor)
            weights *= scale
            weights.loc[crypto_cols[0]] += crypto_floor
        if 'BIL' in weights.index and weights.loc['BIL'] > float(CONFIG['V82_META_BIL_CAP']):
            excess = weights.loc['BIL'] - float(CONFIG['V82_META_BIL_CAP'])
            weights.loc['BIL'] = float(CONFIG['V82_META_BIL_CAP'])
            non_bil = [c for c in weights.index if c != 'BIL']
            if non_bil:
                add_to = pd.Series(1.0, index=non_bil)
                weights.loc[non_bil] += excess * add_to / add_to.sum()
        weights = weights / max(1e-12, weights.sum())
        daily.loc[days] = stream_df.reindex(days)[weights.index].fillna(0.0).dot(weights).values
        rows.append({'date': dt, 'next_date': next_dt, **{f'w_{k}': float(v) for k, v in weights.items() if abs(v) > 1e-12}})
    return daily.fillna(0.0), pd.DataFrame(rows)


def build_v82_hrp_lite_allocator():
    """Correlation-penalized inverse-vol allocator. HRP-lite without extra dependencies."""
    candidates = v82_candidate_stream_cols()
    daily = pd.Series(0.0, index=stream_df.index, name='V82_HRP_LITE_ALLOCATOR')
    rows = []
    for i, dt in enumerate(alloc_dates):
        dt = pd.Timestamp(dt)
        next_dt = pd.Timestamp(alloc_dates[i + 1]) if i + 1 < len(alloc_dates) else stream_df.index.max()
        days = stream_df.index[(stream_df.index > dt) & (stream_df.index <= next_dt)]
        if len(days) == 0:
            continue
        past = stream_df[candidates].loc[:dt].tail(126).replace([np.inf, -np.inf], np.nan).fillna(0.0)
        if len(past) < 30:
            weights = pd.Series(0.0, index=candidates)
            if 'BIL' in weights.index:
                weights.loc['BIL'] = 1.0
        else:
            vol = past.std().replace(0, np.nan)
            inv = (1.0 / vol).replace([np.inf, -np.inf], np.nan).fillna(0.0)
            corr = past.corr().abs().replace([np.inf, -np.inf], np.nan).fillna(0.0)
            avg_corr = corr.mean().clip(lower=0.05)
            raw = inv / avg_corr
            if raw.sum() <= 0:
                raw = pd.Series(1.0, index=candidates)
            weights = raw / raw.sum()
        # Crypto floor.
        crypto_cols = [c for c in ['CRYPTO_CASH_GATED', 'CRYPTO_BTC_ETH_CORE', 'CRYPTO_LOW_VOL_CORE', 'CRYPTO_RISK_PARITY'] if c in weights.index]
        crypto_floor = float(CONFIG['V82_META_CRYPTO_FLOOR'])
        crypto_now = float(weights.reindex(crypto_cols).fillna(0.0).sum()) if crypto_cols else 0.0
        if crypto_cols and crypto_now < crypto_floor:
            weights *= max(0.0, 1.0 - crypto_floor)
            weights.loc[crypto_cols[0]] += crypto_floor
        weights = weights / max(1e-12, weights.sum())
        daily.loc[days] = stream_df.reindex(days)[weights.index].fillna(0.0).dot(weights).values
        rows.append({'date': dt, 'next_date': next_dt, **{f'w_{k}': float(v) for k, v in weights.items() if abs(v) > 1e-12}})
    return daily.fillna(0.0), pd.DataFrame(rows)



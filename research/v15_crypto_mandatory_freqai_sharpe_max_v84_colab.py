def simulate_v8_ppo_policy(model, decision_dates, variant_name='V8_PPO'):
    daily = pd.Series(0.0, index=stream_df.index, dtype=float, name=variant_name)
    rows = []
    last_action_idx = 0
    for dt in [pd.Timestamp(d) for d in decision_dates]:
        if dt not in v8_state_df.index:
            continue
        obs = v8_obs_for_date(dt, last_action_idx)
        action, _ = model.predict(obs, deterministic=True)
        action = int(action)
        raw_action_name = V8_ACTION_NAMES[action]
        next_dt = pd.Timestamp(v8_state_df.loc[dt, 'next_date'])
        days = stream_df.index[(stream_df.index > dt) & (stream_df.index <= next_dt)]
        action_name = v82_effective_action_name(raw_action_name, days) if 'v82_effective_action_name' in globals() else raw_action_name
        if len(days) == 0:
            continue
        ret = v8_action_interval_return(action_name, days).copy()
        switched = action != last_action_idx
        if switched:
            ret.iloc[0] -= float(CONFIG['V8_PPO_SWITCH_COST_BPS']) / 10000.0
        daily.loc[days] = ret.values
        rows.append({'date': dt, 'next_date': next_dt, 'action_idx': action, 'raw_action': raw_action_name, 'action': action_name, 'switched': switched, 'interval_return': v8_compound(ret), 'hurdle_return': v8_hurdle_return(days), 'crypto_exposure': v8_action_crypto_exposure(V8_ACTION_SPECS[action_name]), 'hard_crypto_exposure': v8_action_hard_crypto_exposure(V8_ACTION_SPECS[action_name]), 'bad_crypto_regime': v8_bad_crypto_regime_for_days(days)})
        last_action_idx = action
    return daily.rename(variant_name), pd.DataFrame(rows)


def action_probabilities_for_obs(model, obs):
    obs_t = torch.as_tensor(obs).float().unsqueeze(0).to(model.device)
    with torch.no_grad():
        dist = model.policy.get_distribution(obs_t)
        probs = dist.distribution.probs.detach().cpu().numpy()[0]
    return probs



def v82_action_validation_score(weights):
    """Validation-only score used to prune the action menu before PPO."""
    ret = v8_weighted_stream_return(weights, 'v82_action_candidate')
    stats = perf_stats(ret, CONFIG['VALIDATION_START'], CONFIG['VALIDATION_END'])
    refs = [c for c in ['BIL', 'SPY', 'QQQ', 'TOP_EW', 'CURRENT_EW', 'V8_ALPHA_ENSEMBLE', 'CRYPTO_CASH_GATED'] if c in stream_df.columns]
    diffs = []
    ret_diffs = []
    for ref in refs:
        rs = perf_stats(stream_df[ref], CONFIG['VALIDATION_START'], CONFIG['VALIDATION_END'])
        if pd.notna(stats['sharpe']) and pd.notna(rs['sharpe']):
            diffs.append(stats['sharpe'] - rs['sharpe'])
        if pd.notna(stats['ann_return']) and pd.notna(rs['ann_return']):
            ret_diffs.append(stats['ann_return'] - rs['ann_return'])
    min_diff = min(diffs) if diffs else -9.0
    avg_diff = float(np.mean(diffs)) if diffs else -9.0
    n_beat = int(np.sum(np.array(diffs) > 0)) if diffs else 0
    crypto_expo = v8_action_crypto_exposure(weights)
    hard_crypto = v8_action_hard_crypto_exposure(weights)
    crypto_ls_weight = float(weights.get('CRYPTO_LS', 0.0))
    penalty = 0.0
    if crypto_expo > float(CONFIG['V82_MAX_TERMINAL_CRYPTO_WEIGHT']):
        penalty += 500.0 * (crypto_expo - float(CONFIG['V82_MAX_TERMINAL_CRYPTO_WEIGHT']))
    if crypto_ls_weight > float(CONFIG['V82_MAX_CRYPTO_LS_WEIGHT']):
        penalty += 500.0 * (crypto_ls_weight - float(CONFIG['V82_MAX_CRYPTO_LS_WEIGHT']))
    score = (
        700.0 * n_beat
        + 200.0 * min_diff
        + 90.0 * avg_diff
        + 30.0 * (stats['sharpe'] if pd.notna(stats['sharpe']) else -9.0)
        + 25.0 * (stats['ann_return'] if pd.notna(stats['ann_return']) else -9.0)
        - 25.0 * abs(stats['max_dd'] if pd.notna(stats['max_dd']) else 0.0)
        - penalty
    )
    return float(score), stats, min_diff, avg_diff, n_beat



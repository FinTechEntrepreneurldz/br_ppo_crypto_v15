def crypto_score(dt):
    dt = pd.Timestamp(dt)
    past = crypto_ret.loc[:dt].tail(252)
    if len(past) < int(CONFIG['CRYPTO_MIN_HISTORY_DAYS']):
        return pd.Series(dtype=float)
    mom21 = (1.0 + past.tail(21)).prod() - 1.0
    mom63 = (1.0 + past.tail(63)).prod() - 1.0
    mom126 = (1.0 + past.tail(126)).prod() - 1.0
    vol63 = past.tail(63).std() * np.sqrt(252)
    mom126_ex21 = ((1.0 + past.tail(126)).prod() / (1.0 + past.tail(21)).prod()) - 1.0 if len(past) >= 147 else mom126
    ichi_block = (
        0.45 * crypto_ichi['above_cloud'].loc[dt].reindex(crypto_ret.columns).fillna(0.0) +
        0.35 * crypto_ichi['tenkan_gt_kijun'].loc[dt].reindex(crypto_ret.columns).fillna(0.0) +
        0.20 * robust_z(crypto_ichi['dist_cloud_mid'].loc[dt]).reindex(crypto_ret.columns).fillna(0.0)
    )
    score = (
        0.23 * mom21.rank(pct=True) +
        0.27 * mom63.rank(pct=True) +
        0.20 * mom126.rank(pct=True) +
        0.12 * mom126_ex21.rank(pct=True) -
        0.08 * vol63.rank(pct=True) +
        0.10 * ichi_block.rank(pct=True)
    )
    return score.replace([np.inf, -np.inf], np.nan).dropna()



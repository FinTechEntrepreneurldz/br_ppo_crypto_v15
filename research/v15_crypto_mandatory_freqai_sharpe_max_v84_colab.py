def simulate_v8_ppo_policy(model, decision_dates, variant_name='V8_PPO'):
    daily = pd.Series(0.0, index=stream_df.index, dtype=float, name=variant_name)
    rows = []
    last_action_idx = 0
    last_effective_weights = dict(V8_ACTION_SPECS[V8_ACTION_NAMES[0]])
    for dt in [pd.Timestamp(d) for d in decision_dates]:
        if dt not in v8_state_df.index:
            continue
        obs = v8_obs_for_date(dt, last_action_idx)
        action, _ = model.predict(obs, deterministic=True)
        action = int(action)
        raw_action_name = V8_ACTION_NAMES[action]
        next_dt = pd.Timestamp(v8_state_df.loc[dt, 'next_date'])
        days = stream_df.index[(stream_df.index > dt) & (stream_df.index <= next_dt)]
        action_name = v84_effective_action_name(raw_action_name, days)
        if len(days) == 0:
            continue
        switched = action != last_action_idx
        target_weights = dict(V8_ACTION_SPECS[action_name])
        effective_weights = v84_blend_action_weights(last_effective_weights, target_weights) if switched else target_weights
        ret = v8_weighted_stream_return(effective_weights, name=action_name).reindex(days).fillna(0.0).copy()
        if switched:
            ret.iloc[0] -= float(CONFIG['V8_PPO_SWITCH_COST_BPS']) / 10000.0
        daily.loc[days] = ret.values
        rows.append({'date': dt, 'next_date': next_dt, 'action_idx': action, 'raw_action': raw_action_name, 'action': action_name, 'switched': switched, 'partial_rebalance': bool(CONFIG.get('V84_PARTIAL_REBALANCE_ENABLE', True) and switched), 'interval_return': v8_compound(ret), 'hurdle_return': v8_hurdle_return(days), 'crypto_exposure': v8_action_crypto_exposure(effective_weights), 'hard_crypto_exposure': v8_action_hard_crypto_exposure(effective_weights), 'bad_crypto_regime': v8_bad_crypto_regime_for_days(days), **{f'effective_w_{k}': v for k, v in effective_weights.items()}})
        last_action_idx = action
        last_effective_weights = dict(effective_weights)
    return daily.rename(variant_name), pd.DataFrame(rows)


def simulate_v8_ppo_ensemble_policy(models, decision_dates, variant_name='V8_PPO_ENSEMBLE'):
    daily = pd.Series(0.0, index=stream_df.index, dtype=float, name=variant_name)
    rows = []
    prob_rows = []

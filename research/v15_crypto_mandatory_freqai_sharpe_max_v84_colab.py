def diversified_take(ranked_symbols, k, max_per_sector=5):
    chosen = []
    sec_count = defaultdict(int)
    for s in ranked_symbols:
        if s not in symbols or s in chosen:
            continue
        sec = sectors.get(s, 'Unknown')
        if sec_count[sec] >= max_per_sector:
            continue
        chosen.append(s)
        sec_count[sec] += 1
        if len(chosen) >= k:
            break
    for s in ranked_symbols:
        if len(chosen) >= k:
            break
        if s in symbols and s not in chosen:
            chosen.append(s)
    return chosen[:k]



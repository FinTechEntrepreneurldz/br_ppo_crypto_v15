def build_v84_network_rp_allocator():
    """Network-risk-parity-lite allocator using inverse vol and correlation centrality."""
    candidates = v84_allocator_candidate_cols()
    daily = pd.Series(0.0, index=stream_df.index, name='V84_NETWORK_RP_ALLOCATOR')
    rows = []
    for i, dt in enumerate(alloc_dates):
        dt = pd.Timestamp(dt)
        next_dt = pd.Timestamp(alloc_dates[i + 1]) if i + 1 < len(alloc_dates) else stream_df.index.max()
        days = stream_df.index[(stream_df.index > dt) & (stream_df.index <= next_dt)]
        if len(days) == 0:
            continue
        past = stream_df[candidates].loc[:dt].tail(126).replace([np.inf, -np.inf], np.nan).fillna(0.0)
        if len(past) < 40:
            weights = pd.Series(0.0, index=candidates)
            if 'BIL' in weights.index:
                weights.loc['BIL'] = 1.0
            else:
                weights[:] = 1.0 / max(1, len(weights))
        else:
            vol = past.std().replace(0, np.nan)
            inv_vol = (1.0 / vol).replace([np.inf, -np.inf], np.nan).fillna(0.0)
            corr = past.corr().replace([np.inf, -np.inf], np.nan).fillna(0.0)
            abs_corr = corr.abs()
            # Centrality penalty: highly connected/crowded streams receive less weight.
            centrality = abs_corr.mean().clip(lower=0.05)
            # Diversification strength: reward streams less correlated to the average.
            raw = inv_vol / centrality
            # Add small trailing-quality tilt, bounded so risk still dominates.
            quality = {}
            for c in candidates:
                x = past[c]
                quality[c] = 0.25 * (annualized_sharpe(x) if len(x) > 20 else 0.0) - 0.50 * abs(min(0.0, max_drawdown(x)))
            q = pd.Series(quality).replace([np.inf, -np.inf], np.nan).fillna(0.0)
            q = (q - q.median()).clip(-1.0, 1.0)
            raw = raw * (1.0 + 0.15 * q)
            raw = raw.clip(lower=0.0)
            if raw.sum() <= 0:
                raw = pd.Series(1.0, index=candidates)
            weights = raw / raw.sum()
        crypto_cols_local = [c for c in ['CRYPTO_FREQAI_META_CASH_GATED', 'CRYPTO_CASH_GATED', 'CRYPTO_BTC_ETH_CORE', 'CRYPTO_LOW_VOL_CORE', 'CRYPTO_RISK_PARITY'] if c in weights.index]
        crypto_floor = float(CONFIG['V84_NETWORK_RP_CRYPTO_FLOOR'])
        crypto_now = float(weights.reindex(crypto_cols_local).fillna(0.0).sum()) if crypto_cols_local else 0.0
        if crypto_cols_local and crypto_now < crypto_floor:
            weights *= max(0.0, 1.0 - crypto_floor)
            weights.loc[crypto_cols_local[0]] += crypto_floor
        if 'BIL' in weights.index and weights.loc['BIL'] > float(CONFIG['V84_NETWORK_RP_MAX_BIL']):
            excess = weights.loc['BIL'] - float(CONFIG['V84_NETWORK_RP_MAX_BIL'])
            weights.loc['BIL'] = float(CONFIG['V84_NETWORK_RP_MAX_BIL'])
            non_bil = [c for c in weights.index if c != 'BIL']
            if non_bil:
                weights.loc[non_bil] += excess / len(non_bil)
        weights = weights / max(1e-12, weights.sum())
        daily.loc[days] = stream_df.reindex(days)[weights.index].fillna(0.0).dot(weights).values
        rows.append({'date': dt, 'next_date': next_dt, **{f'w_{k}': float(v) for k, v in weights.items() if abs(v) > 1e-12}})
    return daily.fillna(0.0), pd.DataFrame(rows)


